
package it.softstrategy.nevis.exceptions;

/**
 * @author lgalati
 *
 */
public class RecordingStorageMountPoolException extends Exception{

	
	
	private static final long serialVersionUID = 1L;

	
	
	public RecordingStorageMountPoolException() {	}

	
	public RecordingStorageMountPoolException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	
	public RecordingStorageMountPoolException(String message, Throwable cause) {
		super(message, cause);
	}

	
	public RecordingStorageMountPoolException(String message) {
		super(message);
	}

	
	public RecordingStorageMountPoolException(Throwable cause) {
		super(cause);
	}
	
	

}
