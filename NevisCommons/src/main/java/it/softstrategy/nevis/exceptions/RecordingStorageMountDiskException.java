package it.softstrategy.nevis.exceptions;

/**
 * @author lgalati
 *
 */
public class RecordingStorageMountDiskException extends Exception {

	private static final long serialVersionUID = 1L;

	public RecordingStorageMountDiskException() {
	}

	public RecordingStorageMountDiskException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public RecordingStorageMountDiskException(String message, Throwable cause) {
		super(message, cause);
	}

	public RecordingStorageMountDiskException(String message) {
		super(message);
	}

	public RecordingStorageMountDiskException(Throwable cause) {
		super(cause);
	}
	
	
	

}
